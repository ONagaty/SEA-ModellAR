package eu.cessar.ct.testutils;

/**
 * Various time constants and related utilities
 *
 * @author uidl6458
 *
 */
public class TimeUtils
{

	/**
	 * Constant representing 0 seconds in milliseconds
	 */
	public static final long TIME_0_S = 0;

	/**
	 * Constant representing 1 second in milliseconds
	 */
	public static final long TIME_1_S = 1000;

	/**
	 * Constant representing 5 seconds in milliseconds
	 */
	public static final long TIME_5_S = 5 * TIME_1_S;

	/**
	 * Constant representing 10 seconds in milliseconds
	 */
	public static final long TIME_10_S = 10 * TIME_1_S;

	/**
	 * Constant representing 20 seconds in milliseconds
	 */
	public static final long TIME_20_S = 20 * TIME_1_S;

	/**
	 * Constant representing 1 minute in milliseconds
	 */
	public static final long TIME_1_M = 60 * TIME_1_S;

	public static final long TIME_5_M = 5 * 60 * TIME_1_S;
}
